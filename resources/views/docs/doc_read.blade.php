    <h3>
        2. 导航和筛选功能
    </h3>
    <hr>
    <p>
        为了方便开发人员阅读和查找文档，每个部分都在页面左侧设立了导航栏，点击即可使页面跳转到对应词条。
        <br>
        其中，API文档部分还提供分组、条件筛选功能。
        <img src="http://cdn.marklux.cn/18-4-22/46122835.jpg" class="img-responsive" alt="Responsive image" style="max-width: 20vw">
        选择组别、筛选依据和顺序后，点击筛选按钮，即可按照所选条件重新排序文档。
    </p>
    <br>
    <h3>
        3. 收藏文档
